/*
    Angular CLI (command-line interface)
        https://angular.io/cli
        Serve para atender linhas de comando para trabalhar com Angular
        Comandos:
            ng new - criar aplicação
            ng generate - criar uma parte da aplicação (componente, rota, serviço e etc.)
            ng serve - para rodar a aplicação localmente
            entre outros

    Assim, para criar uma aplicação:
        ng new nome_da_aplicacao

    src - pasta que contém a parte principal da aplicação
        app - pasta com componentes da app
        assets - pasta com arquivos de complementação, como css e imagens
        environments - pasta com arquivos de gerenciamento da app, ambiente desenvolvimento, produção

        favicon.ico - ícone da app
        index.html - página HTML
        main.ts - arquivo principal da app
        polyfills.ts - configurações que permitem browsers antigos entenderem a app
        styles.css - estilização (pode estar em assets)
        test.ts - para executar testes

    Para rodar a app:
        Feito via terminal (toggle terminal -> ctrl ')
        ng serve ou ng s

        Para parar a aplicação: ctrl c

    Extensões do VSCode que ajudarão no desenvolvimento:
        Angular Language Service
        Angular Snippets
        Better Comments
        npm Intellisense
        vscode-icons
*/