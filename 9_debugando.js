/*
    Maneiras de debugar a aplicação

    Navegador
        Vá em Inspecionar, Sources, selecione arquivo desejado, selecione códigos a serem marcados.
        obs.: modo de desenvolvimento; pasta webpack.

    Visual Studio Code
        Ter alguma extensão, como 'Debugger for Chrome', 'JavaScript Debugger'.
        Vá em Run and Debug.
            Atualizar campo "url": "http://localhost:8080" para a porta utilizada.
        Colocar breakpoints nas linhas de código desejadas.
        Volte em Run and Debug e selecione Launch.
*/